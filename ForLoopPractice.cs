using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Day2ForLoop
{
    class ForLoopPractice
    {
        static void Main(string[] args)
        {
            int i;

            for(i=2; i<101; i+=2)
            {
                Console.WriteLine(i);
            }

            Console.Read();
        }
    }
}
